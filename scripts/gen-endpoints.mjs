#!/usr/bin/env node
// Regenerates the WARP proxy endpoints inside each vendored Clash template,
// in place, between the "# === BEGIN/END WARP PROXIES ===" markers. Everything
// outside those markers (anchors, dns, rule-providers, proxy-groups, rules) is
// left untouched. Idempotent — safe to re-run.
//
//   node scripts/gen-endpoints.mjs      (or: npm run gen:endpoints)
//
// Tune the pool by editing RANGES / PORTS below.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const TEMPLATES_DIR = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
  "config",
  "templates",
);

// One representative IP per known-good Cloudflare WARP range. Extra IPs in the
// same /24 add little (same anycast prefix), so we vary PORTS instead — port
// diversity is what dodges DPI in RU. Replace these if a range stops working.
const RANGES = [
  "162.159.192.1", "162.159.195.1", "8.6.112.1", "8.34.70.1", "8.34.146.1",
  "8.35.211.1", "8.39.125.1", "8.39.204.1", "8.39.214.1", "8.47.69.1",
  "188.114.96.1", "188.114.97.1", "188.114.98.1",
];

// Per device: 4 standard WARP ports + a spread of exotic ones (anti-DPI).
// Bigger list = more fallback candidates but a heavier url-test. Keep router lean.
const PORTS = {
  "computer.yaml": [500, 1701, 2408, 4500, 880, 943, 1002, 2371, 5279, 8886],
  "mobile.yaml":   [500, 1701, 2408, 4500, 2371, 8886],
  "router.yaml":   [500, 2408, 4500, 2371],
};

const plain = (prefix, label, server, port) => [
  `  - name: "[${prefix}] ${label}"`,
  `    <<: *warp-common`,
  `    server: ${server}`,
  `    port: ${port}`,
];
const alt = (n, server, port, i1) => [
  `  - name: "[⭐] (Alt ${n}) ${server}:${port}"`,
  `    <<: *warp-common`,
  `    server: ${server}`,
  `    port: ${port}`,
  `    amnezia-wg-option:`,
  `      <<: *amnezia-base`,
  `      i1: *${i1}`,
];

// Curated anchors: DNS-resilient engage hostname + 3 obfuscation variants.
const ORIGIN = [
  "  # ── origin-endpoint ──",
  ...plain("⭐", "engage.cloudflareclient.com:500", "engage.cloudflareclient.com", 500),
  ...plain("⭐", "engage.cloudflareclient.com:2408", "engage.cloudflareclient.com", 2408),
  ...plain("⭐", "engage.cloudflareclient.com:4500", "engage.cloudflareclient.com", 4500),
  ...alt("1", "engage.cloudflareclient.com", 4500, "i1-alt1"),
  ...alt("2", "162.159.192.1", 4500, "i1-alt2"),
  ...alt("3", "8.6.112.1", 4500, "i1-alt3"),
];

for (const [file, ports] of Object.entries(PORTS)) {
  const full = path.join(TEMPLATES_DIR, file);
  const lines = fs.readFileSync(full, "utf8").split("\n");
  const b = lines.findIndex((l) => l.startsWith("# === BEGIN WARP PROXIES"));
  const e = lines.findIndex((l) => l.startsWith("# === END WARP PROXIES"));
  if (b < 0 || e < 0) {
    console.error(`${file}: BEGIN/END WARP PROXIES markers not found`);
    process.exit(1);
  }

  const cloud = ["  # ── default-endpoint ──"];
  for (const ip of RANGES) for (const p of ports) cloud.push(...plain("☁️", `${ip}:${p}`, ip, p));

  const block = [
    "# === BEGIN WARP PROXIES (vendored: 13 ranges × ports, generated by scripts/gen-endpoints.mjs) ===",
    ...ORIGIN,
    ...cloud,
    "# === END WARP PROXIES ===",
  ];

  fs.writeFileSync(full, [...lines.slice(0, b), ...block, ...lines.slice(e + 1)].join("\n"));
  const cloudCount = RANGES.length * ports.length;
  console.log(`${file}: ⭐=6  ☁️=${cloudCount}  total=${cloudCount + 6}  (${ports.length} ports)`);
}
